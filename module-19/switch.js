var color = 'green';

if (color == 'blue'){
    console.log('color is blue');
}
else if(color == 'red'){
    console.log('color is red');
}
else if(color == 'yellow'){
    console.log('color is yellow');
}
else{
    console.log('color is white');
}
switch(color){
    case 'blue':
        console.log('color is blue');
        break;
    case 'red':
        console.log('color is red');
        break;
    case 'yellow':
        console.log('color is yellow');
        break;
    case 'green':
        console.log('color is green');
        break;
    default:
}